    public function findOneByUserAndEvenement(User $user, Evenement $evenement): ?Reservation
    {
        $reservations = $this->createQueryBuilder('r')
            ->where('r.user = :user')
            ->andWhere('r.evenement = :evenement')
            ->setParameter('user', $user)
            ->setParameter('evenement', $evenement)
            ->getQuery()
            ->getResult();

        return $reservations[0] ?? null;
